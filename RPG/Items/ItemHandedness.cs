namespace StreamRC.RPG.Items {
    /// <summary>
    /// handedness of an item
    /// </summary>
    public enum ItemHandedness {

        /// <summary>
        /// no specific handedness (doesn't apply)
        /// </summary>
        None,

        /// <summary>
        /// item is to be hold in one hand
        /// </summary>
        One,

        /// <summary>
        /// item is to be hold with two hands
        /// </summary>
        Both
    }
}